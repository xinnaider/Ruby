class Servico
  def codigo
    @codigo
  end

  def codigo=(novo_codigo)
    @codigo = novo_codigo
  end

  def nome
    @nome
  end

  def nome=(novo_nome)
    @nome = novo_nome
  end

  def valorEstimado
    @valorEstimado
  end

  def valorEstimado=(novo_valor_estimado)
    @valorEstimado = novo_valor_estimado
  end

  def garantias
    @garantias
  end

  def garantias=(novas_garantias)
    @garantias = novas_garantias
  end

  def tempoEstimado
    @tempoEstimado
  end

  def tempoEstimado=(novo_tempo_estimado)
    @tempoEstimado = novo_tempo_estimado
  end
end
